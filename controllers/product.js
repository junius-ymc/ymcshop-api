exports.create = async (req, res) => {
    try {
        // code
        const { title, description, price, quantity, categoryId, images } = req.body
        // console.log(title, description, price, quantity, images)
        const product = await prisma.product.create({
            data: {
                title: title,
                description: description,
                price: parseFloat(price),
                quantity: parseInt(quantity),
                categoryId: parseInt(categoryId),
                images: {
                    create: images.map((item) => ({
                        asset_id: item.asset_id,
                        public_id: item.public_id,
                        url: item.url,
                        secure_url: item.secure_url
                    }))
                }
            }
        })
        res.send(product)
    } catch (err) {
        console.log(err)
        res.status(500).json({ message: "Server error" })
    }
}
exports.list = async (req, res) => {
    try {
        // code
        // ‚úÖ ‡∏£‡∏±‡∏ö‡∏Ñ‡πà‡∏≤ count ‡πÅ‡∏•‡∏∞ page ‡∏à‡∏≤‡∏Å query parameters
        const { count = 4, page = 1 } = req.query;
        const limit = parseInt(count);
        const skip = (parseInt(page) - 1) * limit;

        // ‡∏ô‡∏±‡∏ö‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î
        const totalProducts = await prisma.product.count();
        const totalPages = Math.ceil(totalProducts / limit); // ‚úÖ ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏´‡∏ô‡πâ‡∏≤ (‡∏Ñ‡πà‡∏≤‡∏ó‡∏µ‡πà‡∏™‡πà‡∏á‡∏°‡∏≤‡∏à‡∏≤‡∏Å‡∏´‡∏ô‡πâ‡∏≤‡∏ö‡πâ‡∏≤‡∏ô ‡∏ä‡∏¥‡πâ‡∏ô‡∏ï‡πà‡∏≠ 1 ‡∏´‡∏ô‡πâ‡∏≤)

        // ‡∏î‡∏∂‡∏á‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤‡∏ï‡∏≤‡∏°‡∏´‡∏ô‡πâ‡∏≤
        const products = await prisma.product.findMany({
            take: limit,
            skip: skip,
            orderBy: { createdAt: "desc" }, // ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏•‡∏≥‡∏î‡∏±‡∏ö‡πÇ‡∏î‡∏¢ createdAt ‡∏à‡∏≤‡∏Å‡πÉ‡∏´‡∏°‡πà‡πÑ‡∏õ‡πÄ‡∏Å‡πà‡∏≤
            include: { category: true, images: true },
        })
        // // ‚úÖ ‡∏™‡πà‡∏á‡∏Ñ‡πà‡∏≤‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏´‡∏ô‡πâ‡∏≤‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ
        res.send({
            products,
            totalPages,
            currentPage: parseInt(page),
            itemsPerPage: limit
        });
    } catch (err) {
        console.log(err)
        res.status(500).json({ message: "Server error" })
    }
}
