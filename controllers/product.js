exports.getProductPage = async (req, res) => {
    const { productId } = req.params;
    const { itemsPerPage = 4 } = req.query;

    try {
        // ‡∏´‡∏≤‡∏•‡∏≥‡∏î‡∏±‡∏ö‡∏Ç‡∏≠‡∏á‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤‡πÉ‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î
        const allProducts = await prisma.product.findMany({
            orderBy: { createdAt: "desc" }, // ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏•‡∏≥‡∏î‡∏±‡∏ö‡∏ï‡∏≤‡∏° createdAt ‡∏à‡∏≤‡∏Å‡πÉ‡∏´‡∏°‡πà‡πÑ‡∏õ‡πÄ‡∏Å‡πà‡∏≤
        });
        const productIndex = allProducts.findIndex(p => p.id === parseInt(productId));

        if (productIndex === -1) {
            return res.status(404).json({ error: 'Product not found' });
        }

        // ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏´‡∏ô‡πâ‡∏≤
        const page = Math.floor(productIndex / itemsPerPage) + 1;
        console.log("üì¶ ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏´‡∏ô‡πâ‡∏≤‡πÑ‡∏î‡πâ:", page); // ‚úÖ ‡πÄ‡∏û‡∏¥‡πà‡∏° log ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö
        res.json({ page }); // ‚úÖ ‡∏™‡πà‡∏á‡∏Ñ‡πà‡∏≤‡∏Å‡∏•‡∏±‡∏ö‡πÄ‡∏õ‡πá‡∏ô JSON
    } catch (error) {
        res.status(500).json({ error: 'Failed to calculate page' });
    }
};